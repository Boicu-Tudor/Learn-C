#include <stdio.h>
#include <stdlib.h>

int main()
{
/*
*C program to check whether a number is even or odd using if else.
*/

    int x;
    printf("Enter a number:");
    scanf("%d", &x);

    if (x%2==0)
    {
        printf("The number is even");
    }else
    {
        printf("The number is odd");
    }





    return 0;
}
